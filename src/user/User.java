package user;

public class User {
	
	private String userID;
	private String userPassword;
	private String userName;
	private String userGender;
	private int userfirPhone;
	private int usersecPhone;
	private int userthrPhone;
	private String userEmail;
	private String userAddress;
	private String userManager;
	
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getUserPassword() {
		return userPassword;
	}
	public void setUserPassword(String userPassword) {
		this.userPassword = userPassword;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getUserGender() {
		return userGender;
	}
	public void setUserGender(String userGender) {
		this.userGender = userGender;
	}
	public int getUserfirPhone() {
		return userfirPhone;
	}
	public void setUserfirPhone(int userfirPhone) {
		this.userfirPhone = userfirPhone;
	}
	public int getUsersecPhone() {
		return usersecPhone;
	}
	public void setUsersecPhone(int usersecPhone) {
		this.usersecPhone = usersecPhone;
	}
	public int getUserthrPhone() {
		return userthrPhone;
	}
	public void setUserthrPhone(int userthrPhone) {
		this.userthrPhone = userthrPhone;
	}
	public String getUserEmail() {
		return userEmail;
	}
	public void setUserEmail(String userEmail) {
		this.userEmail = userEmail;
	}
	public String getUserAddress() {
		return userAddress;
	}
	public void setUserAddress(String userAddress) {
		this.userAddress = userAddress;
	}
	public String getUserManager() {
		return userManager;
	}
	public void setUserManager(String userManager) {
		this.userManager = userManager;
	}

}
